# Inicializamos el contador en 1 (el primer número natural)
contador = 1
limite = 10

print("--- Primeros 10 Números Naturales ---")

while contador <= limite:
    print(contador)
    # Incrementamos el contador en 1 para avanzar en la secuencia
    contador += 1
